#include <unistd.h>

void	ft_putendl(char const *s)
{
	char c;

	c = '\n';
	if (s != NULL)
	{
		while (*s != '\0')
		{
			write(1, s, 1);
			s++;
		}
		write(1, &c, 1);
	}
}
