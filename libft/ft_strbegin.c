#include <string.h>

int		ft_strbegin(char const *s)
{
	int i;

	i = 0;
	if (s == NULL)
		return (0);
	while ((s[i] == ' ' || s[i] == '\n' || s[i] == '\t') && s[i] != '\0')
		i++;
	return (i);
}
